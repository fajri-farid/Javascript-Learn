// sin cos tan
const nilai = 0;
console.log(Math.sin(nilai));
console.log(Math.cos(nilai));
console.log(Math.tan(nilai));

// logaritma
console.log(Math.log10(10)); // 10 log 10 = 1
console.log(Math.log10(30)); // 30 log 10
console.log(Math.log2(4)); // ini 2 log 4

// absolute
console.log(Math.abs(-5)); // karena absolute itu pasti positif maka -5 akan jadi 5
